export class Employee {

    employeeName: string;
    empId: string;
    age: number;
    gender: string;

    constructor(employeeName: string, empId: string, age: number, gender: string) {
        this.employeeName = employeeName;
        this.empId = empId;
        this.age = age;
        this.gender = gender;
    }
}


